CREATE OR REPLACE TABLE weather_data (
    city        VARCHAR(100),
    temperature FLOAT,
    humidity    INT,
    weather     VARCHAR(100),
    time        VARCHAR(50)
);
CREATE OR REPLACE STAGE weather_stage
    URL = 's3://weather-data-121/'
    STORAGE_INTEGRATION = s3_int
    FILE_FORMAT = (TYPE = 'JSON');

CREATE OR REPLACE PIPE weather_pipe
AUTO_INGEST = TRUE
AS
COPY INTO weather_data
FROM (
    SELECT
        $1:city::STRING,
        $1:temperature::FLOAT,
        $1:humidity::INT,
        $1:weather::STRING,
        $1:time::TIMESTAMP
    FROM @weather_stage
)
FILE_FORMAT = (TYPE = 'JSON');




































































































































































































































































































































































































































































































































































































































































































































































































